export function getRandomUnique<T>(arr: T[], count: number): T[] {
    // Shuffle a copy of the array and take the first 'count' elements
    return arr
        .slice()
        .sort(() => 0.5 - Math.random())
        .slice(0, Math.min(count, arr.length));
}

export function getCombinedEntries(n: number, k: number, i: number): string[] {
    const selectedGenres = getRandomUnique(genres, n);
    const selectedBest = getRandomUnique(bestEntries, k);
    const selectedSub = getRandomUnique(subEntries, i);
    return [...mainEntries, ...selectedGenres, ...selectedBest, ...selectedSub];
}

export function getRandomInteger(min: number, max: number): number {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}